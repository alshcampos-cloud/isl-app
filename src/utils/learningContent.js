export const LEARNING_MODULES = [module1, module2, module3, module4, module5];

/**
 * Get a specific lesson by its ID (e.g., "2-3")
 */
export function getLessonById(lessonId) {
  for (const mod of LEARNING_MODULES) {
    const lesson = mod.lessons.find(l => l.id === lessonId);
    if (lesson) return { module: mod, lesson };
  }
  return null;
}

/**
 * Get the next lesson after a given lesson ID
 * Returns null if the given lesson is the last one
 */
export function getNextLesson(currentLessonId) {
  const allLessons = LEARNING_MODULES.flatMap(m =>
    m.lessons.map(l => ({ moduleId: m.id, lessonId: l.id }))
  );
  const currentIdx = allLessons.findIndex(l => l.lessonId === currentLessonId);
  if (currentIdx === -1 || currentIdx === allLessons.length - 1) return null;

  const next = allLessons[currentIdx + 1];
  return getLessonById(next.lessonId);
}

/**
 * Get total lesson count
 */
export function getTotalLessonCount() {
  return LEARNING_MODULES.reduce((sum, m) => sum + m.lessons.length, 0);
}

/**
 * Estimate audio duration from script lines
 * ~150 words per minute at 1.0x speed, pauses add ~2s each
 */
export function estimateAudioDuration(lines) {
  const wordCount = lines.filter(l => l !== '...').join(' ').split(/\s+/).length;
  const pauseCount = lines.filter(l => l === '...').length;
  const speakingSeconds = (wordCount / 150) * 60;
  const pauseSeconds = pauseCount * 2;
  return Math.ceil((speakingSeconds + pauseSeconds) / 60); // in minutes
}
